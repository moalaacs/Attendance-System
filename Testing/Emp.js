export class Employee {
  Name;
  Age;
  Department;
  Position;
  Attendence_info;

  constructor(_name, _age, _department, _position, _attendenceInfo) {
    this.Name = _name;
    this.Age = _age;
    this.Department = _department;
    this.Position = _position;
    this.Attendence_info = _attendenceInfo;
  }
}
